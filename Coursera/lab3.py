#!/usr/bin/env python3

import json
import locale
import sys


def load_data(filename):
  """Loads the contents of filename as a JSON file."""
  with open(filename) as json_file:
    data = json.load(json_file)
  return data


def format_car(car):
  """Given a car dictionary, returns a nicely formatted name."""
  return "{} {} ({})".format(
      car["car_make"], car["car_model"], car["car_year"])


def process_data(data):
  """Analyzes the data, looking for maximums.

  Returns a list of lines that summarize the information.
  """
  max_revenue = {"revenue": 0}
  max_sales = {"total_sales": 0}
  most_popular = {"car_year": 0, "total_sales_in_that_year": 0}
  # dict_popular:: key=>year, value=>total_sales
  dict_popular = {}
  for item in data:
    # Calculate the revenue generated by this model (price * total_sales)
    # We need to convert the price from "$1234.56" to 1234.56
    item_price = locale.atof(item["price"].strip("$"))
    item_revenue = item["total_sales"] * item_price
    if item_revenue > max_revenue["revenue"]:
      item["revenue"] = item_revenue
      max_revenue = item
    # TODO: also handle max sales
    print(item["total_sales"])
    print(max_sales["total_sales"])
    if item["total_sales"] > max_sales["total_sales"]:
      max_sales = item
    # TODO: also handle most popular car_year
    if item["car"]["car_year"] in dict_popular.keys():
      dict_popular[item["car"]["car_year"]] += int(item["total_sales"])
    else:
      dict_popular[item["car"]["car_year"]] = int(item["total_sales"])
        
  for key in dict_popular.keys():
    if dict_popular[key] > most_popular["total_sales_in_that_year"]:
      most_popular["car_year"] = key
      most_popular["total_sales_in_that_year"] = dict_popular[key]


  summary = [
    "The {} generated the most revenue: ${}\n".format(format_car(max_revenue["car"]), max_revenue["revenue"]),
    "The {} had the most sales: {}\n".format(max_sales["car"]["car_model"], max_sales["total_sales"]),
    "The most popular year was {} with {} sales.".format(most_popular["car_year"], most_popular["total_sales_in_that_year"])      
  ]
  print("DEBUG::summary , {}".format(summary))

  return summary


def cars_dict_to_table(car_data):
  """Turns the data in car_data into a list of lists."""
  table_data = [["ID", "Car", "Price", "Total Sales"]]
  for item in car_data:
    table_data.append([item["id"], format_car(item["car"]), item["price"], item["total_sales"]])
  return table_data


